<script setup>
import { router, useForm, usePage } from "@inertiajs/vue3";
import { handleSubmit } from "@/helpers/client-req-handler";
import { scrollToFirstErrorField } from "@/helpers/utils";
import { createOptions } from "@/helpers/options";
import LocaleNumberInput from "@/components/LocaleNumberInput.vue";
import CheckBox from "@/components/CheckBox.vue";
import { onMounted, watch } from "vue";

const page = usePage();
const title = (!!page.props.data.id ? "Edit" : "Tambah") + " Akun / Kas";
const form = useForm({
  id: page.props.data.id,
  name: page.props.data.name,
  type: page.props.data.type,
  bank: page.props.data.bank,
  number: page.props.data.number,
  holder: page.props.data.holder,
  balance: parseFloat(page.props.data.balance),
  active: !!page.props.data.active,
  has_wallet_access: !!page.props.data.has_wallet_access,
  show_in_pos_payment: !!page.props.data.show_in_pos_payment,
  show_in_purchasing_payment: !!page.props.data.show_in_purchasing_payment,
  notes: page.props.data.notes,
});

const types = createOptions(window.CONSTANTS.FINANCE_ACCOUNT_TYPES);

const submit = () =>
  handleSubmit({ form, url: route("admin.finance-account.save") });

const updateShowInPosPaymentState = (type) => {
  if (type === "cashier_cash") {
    form.show_in_pos_payment = true;
  }
};

onMounted(() => {
  updateShowInPosPaymentState(form.type);
});

watch(
  () => form.type,
  (newVal) => {
    updateShowInPosPaymentState(newVal);
  }
);
</script>

<template>
  <i-head :title="title" />
  <authenticated-layout>
    <template #title>{{ title }}</template>
    <template #left-button>
      <div class="q-gutter-sm">
        <q-btn
          icon="arrow_back"
          dense
          color="grey-7"
          flat
          rounded
          @click="$inertia.get(route('admin.finance-account.index'))"
        />
      </div>
    </template>
    <q-page class="row justify-center">
      <div class="col col-md-6 q-pa-xs">
        <q-form
          class="row"
          @submit.prevent="submit"
          @validation-error="scrollToFirstErrorField"
        >
          <q-card square flat bordered class="col">
            <q-card-section class="q-pt-none">
              <input type="hidden" name="id" v-model="form.id" />
              <q-input
                autofocus
                v-model.trim="form.name"
                label="Nama Akun"
                lazy-rules
                :error="!!form.errors.name"
                :disable="form.processing"
                :error-message="form.errors.name"
                :rules="[
                  (val) => (val && val.length > 0) || 'Nama harus diisi.',
                ]"
                hide-bottom-space
              />
              <q-select
                v-model="form.type"
                label="Jenis Akun"
                :options="types"
                map-options
                emit-value
                :error="!!form.errors.type"
                :disable="form.processing"
                hide-bottom-space
              >
              </q-select>
              <template v-if="form.type == 'bank'">
                <q-input
                  v-model.trim="form.bank"
                  label="Nama Bank"
                  lazy-rules
                  :error="!!form.errors.bank"
                  :disable="form.processing"
                  :error-message="form.errors.bank"
                  hide-bottom-space
                />
                <q-input
                  v-model.trim="form.number"
                  label="Nomor Rekening"
                  lazy-rules
                  :error="!!form.errors.number"
                  :disable="form.processing"
                  :error-message="form.errors.number"
                  hide-bottom-space
                />
                <q-input
                  v-model.trim="form.holder"
                  label="Pemilik Rekening"
                  lazy-rules
                  :error="!!form.errors.holder"
                  :disable="form.processing"
                  :error-message="form.errors.holder"
                  hide-bottom-space
                />
              </template>
              <LocaleNumberInput
                v-model:modelValue="form.balance"
                label="Saldo"
                lazyRules
                :disable="form.processing"
                :allow-negative="true"
                :error="!!form.errors.balance"
                :errorMessage="form.errors.balance"
                :rules="[]"
              />
              <CheckBox
                v-model="form.show_in_pos_payment"
                :disable="form.processing || form.type == 'cashier_cash'"
                label="Tampilkan di Pembayaran Penjualan"
              />
              <CheckBox
                v-model="form.show_in_purchasing_payment"
                :disable="form.processing"
                label="Tampilkan di Pembayaran Pembelian"
              />
              <CheckBox
                v-model="form.has_wallet_access"
                :disable="form.processing"
                label="Tampilkan di Konfirmasi Topup Wallet"
              />
              <CheckBox
                v-model="form.active"
                :disable="form.processing"
                label="Aktif"
              />
              <q-input
                v-model.trim="form.notes"
                type="textarea"
                autogrow
                counter
                maxlength="200"
                label="Catatan"
                lazy-rules
                :disable="form.processing"
                :error="!!form.errors.notes"
                :error-message="form.errors.notes"
                hide-bottom-space
              />
            </q-card-section>
            <q-card-section class="q-gutter-sm">
              <q-btn
                icon="save"
                type="submit"
                label="Simpan"
                color="primary"
                :disable="form.processing"
              />
              <q-btn
                icon="cancel"
                label="Batal"
                :disable="form.processing"
                @click="router.get(route('admin.finance-account.index'))"
              />
            </q-card-section>
          </q-card>
        </q-form>
      </div>
    </q-page>
  </authenticated-layout>
</template>
